package com.saulms.verdantrealm.world;

public class GroundItem {

    private GroundItemType id;
    private int x, y;

    public GroundItemType getId() {
        return id;
    }

    public void setId(GroundItemType id) {
        this.id = id;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
