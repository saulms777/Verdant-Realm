package com.saulms.cavernwarrior;

public class Camera {

    private double x;
    private double y;
    private final double width;
    private final double height;

    public Camera(double x, double y, double width, double height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

}
